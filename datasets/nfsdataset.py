import glob

sys.path.append("external/pytracking")
from pytracking.evaluation.data import Sequence, BaseDataset, SequenceList


def NFSDataset(fps=240):
    return NFSDatasetClass(fps).get_sequence_list()


class NFSDatasetClass(BaseDataset):
    """ NFS dataset.

    Publication:
        Need for Speed: A Benchmark for Higher Frame Rate Object Tracking
        H. Kiani Galoogahi, A. Fagg, C. Huang, D. Ramanan, and S.Lucey
        ICCV, 2017
        http://openaccess.thecvf.com/content_ICCV_2017/papers/Galoogahi_Need_for_Speed_ICCV_2017_paper.pdf

    Download the dataset from http://ci2cv.net/nfs/index.html
    """

    def __init__(self, fps):
        super().__init__()
        self.fps = fps
        self.base_path = self.env_settings.nfs_path
        self.sequence_info_list = self._get_sequence_info_list()

    def get_sequence_list(self):
        return SequenceList(
            [self._construct_sequence(s) for s in self.sequence_info_list]
        )

    def _construct_sequence(self, sequence_info):
        sequence_path = sequence_info["path"]
        ext = sequence_info["ext"]

        init_omit = 0
        if "initOmit" in sequence_info:
            init_omit = sequence_info["initOmit"]

        frames = sorted(
            glob.glob(
                "{base_path}/{sequence_path}/{fps}/{sequence_path}/*.{ext}".format(
                    base_path=self.base_path,
                    sequence_path=sequence_path,
                    fps=self.fps,
                    ext=ext,
                )
            )
        )

        anno_path = "{base_path}/{sequence_path}/{fps}/{anno_path}".format(
            base_path=self.base_path,
            sequence_path=sequence_path,
            fps=self.fps,
            anno_path=sequence_info["anno_path"],
        )

        try:
            ground_truth_rect = np.loadtxt(str(anno_path), dtype=str)
        except:
            ground_truth_rect = np.loadtxt(
                str(anno_path), delimiter=",", dtype=str
            )

        ground_truth_rect = ground_truth_rect[:, 1:5].astype(float)  # [left, top, right, bottom]
        ground_truth_rect[:, 2:] -= ground_truth_rect[
            :, :2
        ]  # [left, top, width, height]

        if not len(frames) == len(ground_truth_rect):
            if abs(len(ground_truth_rect) / len(frames) - 8) < 1:
                ground_truth_rect = ground_truth_rect[0::8, :]
            diff = abs(len(frames) - len(ground_truth_rect))
            if diff > 0 and diff <= 1:
                n = min(len(frames), len(ground_truth_rect))
                ground_truth_rect = ground_truth_rect[:n]
                frames = frames[:n]
        assert len(frames) == len(ground_truth_rect), sequence_info["name"]

        return Sequence(sequence_info["name"], frames, ground_truth_rect[init_omit:, :])

    def __len__(self):
        """Overload this function in your evaluation. This should return number of sequences in the evaluation """
        return len(self.sequence_info_list)

